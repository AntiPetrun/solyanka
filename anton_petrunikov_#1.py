# –ò—Å—Ö–æ–¥–Ω–∞—è —Å—Ç—Ä–æ–∫–∞
string = '–ù–µ –∑–Ω–∞—é, –∫–∞–∫ —Ç–∞–º –≤ –õ–æ–Ω–¥–æ–Ω–µ, —è –Ω–µ –±—ã–ª–∞. –ú–æ–∂–µ—Ç, —Ç–∞–º —Å–æ–±–∞–∫–∞ ‚Äî –¥—Ä—É–≥ —á–µ–ª–æ–≤–µ–∫–∞. –ê —É –Ω–∞—Å —É–ø—Ä–∞–≤–¥–æ–º ‚Äî –¥—Ä—É–≥ —á–µ–ª–æ–≤–µ–∫–∞!'

'''
    1. –ü–æ–¥—Å—á–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ —Å–∏–º–≤–æ–ª–æ–≤ –≤ –∏—Å—Ö–æ–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–µ.
'''

quant_symbols = len(string)
print(f'1. –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–∏–º–≤–æ–ª–æ–≤ –≤ —Å—Ç—Ä–æ–∫–µ - {quant_symbols}')

'''
    2. –ü–µ—Ä–µ–≤–µ—Ä–Ω—É—Ç—å —Å—Ç—Ä–æ–∫—É.
'''

rev_str = string[::-1]
print(f'2. –ü–µ—Ä–µ–≤–µ—Ä–Ω—É—Ç–∞—è —Å—Ç—Ä–æ–∫–∞ - {rev_str}')

'''
    3. –°–¥–µ–ª–∞—Ç—å –∫–∞–∂–¥–æ–µ —Å–ª–æ–≤–æ —Å –±–æ–ª—å—à–æ–π –±—É–∫–≤—ã.
'''

first_symbol_up = string.title()
print(f'3. –°—Ç—Ä–æ–∫–∞, –≥–¥–µ –≤—Å–µ —Å–ª–æ–≤–∞ —Å –∑–∞–≥–ª–∞–≤–Ω–æ–π –±—É–∫–≤—ã - {first_symbol_up}')

'''
    4. –°–¥–µ–ª–∞—Ç—å –≤–µ—Å—å —Ç–µ–∫—Å—Ç –ø—Ä–æ–ø–∏—Å–Ω—ã–º–∏ –±—É–∫–≤–∞–º–∏.
'''

low_str = string.lower()
print(f'4. –¢–µ–∫—Å—Ç –≤ –Ω–∏–∂–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ (–≤—Å–µ —Å –ø—Ä–æ–ø–∏—Å–Ω–æ–π –±—É–∫–≤—ã) - {low_str}')

'''
    5. –ù–∞–π—Ç–∏ —á–∏—Å–ª–æ –≤—Ö–æ–∂–¥–µ–Ω–∏–π "–Ω–¥", "–∞–º", "–æ" –≤ —Å—Ç—Ä–æ–∫—É.
'''

find_one, find_two, find_three = string.count('–Ω–¥'), string.count('–∞–º'), string.count('–æ')  # –ø–æ–∑–∏—Ü–∏–æ–Ω–Ω–æ–µ –ø—Ä–∏—Å–≤–∞–∏–≤–∞–Ω–∏–µ
print(f'5. –ß–∏—Å–ª–æ –≤—Ö–æ–∂–¥–µ–Ω–∏–π "–Ω–¥" - {find_one},\
    \n\t–ß–∏—Å–ª–æ –≤—Ö–æ–∂–¥–µ–Ω–∏–π "–∞–º" - {find_two},\
    \n\t–ß–∏—Å–ª–æ –≤—Ö–æ–∂–¥–µ–Ω–∏–π "–æ" - {find_three}')

'''
    6. –°–æ–±—Å—Ç–≤–µ–Ω–Ω—ã–µ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏—è.
'''

snow_str = '*'.join(string.upper().split()) # —Å–∏–º–≤–æ–ª—ã –≤ —Å—Ç—Ä–æ–∫–µ –ø—Ä–∏–≤–µ–ª–∏ –≤ –≤—ã—Å—à–∏–π —Ä–µ–≥–∏—Å—Ç—Ä, —Ä–∞–∑–±–∏–ª–∏ –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω—ã–µ —Å–ª–æ–≤–∞, –ø–æ—Å–ª–µ –≤—Å–µ —Å–∫–ª–µ–∏–ª–∏ "–∑–≤–µ–∑–¥–æ—á–∫–æ–π"
print(snow_str + 'üò±')

'''
    7. –í—ã–≤–µ—Å—Ç–∏ –≤ –∫–æ–Ω—Å–æ–ª—å –∏—Å—Ö–æ–¥–Ω—É—é —Å—Ç—Ä–æ–∫—É.
'''

print(f'7. –ò—Å—Ö–æ–¥–Ω–∞—è —Å—Ç—Ä–æ–∫–∞ - "{string}"')
